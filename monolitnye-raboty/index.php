				<h2 style="font: 32px/32px 'Six Caps';color: #3e3d3d;margin: 0 0 22px 0;">Этапы производства монолитных работ</h2>
				<ul>
					<li>Проводятся подготовительные работы, которые включают уборку территории и подготовку площади для размещения строительного оборудования. Особенность монолитного строительства заключается в том, что бетон для заливки изготавливается на месте возведения здания. </li>
					<li>Обустраивается фундамент и арматурный каркас будущего сооружения. Качественное производство работ на этой стадии гарантирует прочность будущего объекта. </li>
					<li>Монтаж опалубочной системы. Для монолитных работ используются два вида опалубки: туннельная и щитовая. Первая применяется для устройства блоков помещений, так как одновременно возводит внутренние стены и перекрытия требуемых размеров. Площадь квартиры при строительстве с использованием такой опалубки не превышают 60 кв. метров. Второй вариант требует больше времени на монтаж, но в итоге конструкция получается более мобильная. Щитовая опалубка позволяет строить здания любой планировки и площади. </li>
					<li>Заливка бетонной смеси. Для производства монолитных работ используется бетон тяжелых и средних марок. С помощью раствора и опалубки формируются стены будущего строения. </li>
					<li>Прогрев бетонной конструкции. Этот этап в технологии монолитных работ целесообразно проводить в зимний период года, чтобы раствор лучше застывал. </li>
					<li>Демонтаж опалубки. Чтобы бетон застыл, конструкцию оставляют в исходном состоянии на несколько дней. После затвердевания опалубку снимают.</li>
					<li>Внешняя отделка фасада здания. Завершающий этап монолитных работ — декорирование бетонных стен снаружи. Так как поверхности сооружений не нуждаются в выравнивании и производстве тепло- и гидроизоляции, то ограничиваются облицовкой панелями, декоративной штукатуркой или кирпичом. </li>
				</ul>
