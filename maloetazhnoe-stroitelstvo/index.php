<p>Далее выполняется следующий комплекс работ:</p>
<ul>
<li>Общестроительные мероприятия. К ним относят земляные работы, обустройство фундамента, возведение стеновых конструкций, монтаж этажных, дверных и оконных перекрытий. В зависимости от технологии строительства используются соответствующие материалы: бетон, металлический каркас, брусовые панели или оцилиндрованные бревна, кирпич, газоблоки и другие. </li>
<li>Кровельные работы. На этом этапе малоэтажного строительства монтируют стропильную систему, обустраивают обрешетку, утепляют мансарду, укладывают кровельное покрытие, устанавливают системы дымоотведения, снегозадержания, водоотведения. </li>
<li>Инженерные коммуникации. К дому подводятся системы жизнеобеспечения: канализация, водопровод, электричество, отопление, связь, газ.</li>
<li>Отделочные работы. Один из самых трудоемких и сложных этапов малоэтажного строительства. Построенной конструкции придается привлекательный внешний и внутренний вид. </li>
<li>Благоустройство территории. Внешний вид дома должен гармонировать с окружением. Наши специалисты помогут обустроить приусадебную территорию в едином стиле. </li>
</ul>
