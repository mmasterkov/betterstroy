<h2 style="font-size:16px;margin-bottom:0px">Виды ремонта квартиры </h2>
<ul>
<li><span style='font-weight:bold'>Косметический ремонт</span> — обновление внутренней отделки, декоративного покрытия стен, полов и потолков квартиры. Этот наиболее простой и наименее затратный вид ремонтных работ. Мастера обновляют обои, напольное и потолочное покрытия, меняют розетки, светильники, выключатели. Косметический ремонт целесообразен в тех случаях, когда не требуется выравнивать имеющиеся поверхности в квартире. Опционально проводятся шпаклевочные работы, замена коммуникаций и сантехнических приборов.</li>
<li><span style='font-weight:bold'>Капитальный ремонт</span>. Он включает демонтаж старых покрытий, замену проводки, прокладку новых коммуникаций и подключение сантехники, выравнивание поверхностей пола, стен и потолка, укладку декоративной облицовки. Дополнительно может быть выполнена перепланировка помещений. </li>
</ul>
<p>Существует несколько разновидностей капитального ремонта квартиры: со стандартным или улучшенным видом, евроремонт. Все работы в обязательном порядке выполняются по дизайн-проекту и зачастую включают перепланировку комнат. </p>
