<p>
				 Перед нашей компанией была поставлена задача устроить утепленный мокрый фасад в коттедже, расположенном в селе Аксиньино в Одинцовском районе МО. Площадь объекта — 180 кв. метров.
			</p>
			<p>
				 Мы использовали следующую технологию строительства теплого фасада:
			</p>
			<ul>
				<li>приклеили на стены базальтового утеплителя плотностью 120 кг на куб. метр. Чтобы теплоизолятор надежно держался на вертикальной поверхности, в качестве крепежа использовали дюбель-гвоздь;</li>
				<li>нанесли базовый и армирующий слой штукатурки с тепловой сеткой;</li>
				<li>использовали для финишного слоя напыляемую эластичную штукатурку. </li>
			</ul>
			<p>
				 Ключевые этапы обустройства фасада показаны на фото объекта. На все работы потребовалось 2 недели.
			</p>

