<p>Заказчик поставил перед нами задачу полностью обновить фасад дома с удалением старой штукатурки в селе Дедёшено Московской области. Площадь объекта — 380 кв. метров. </p>
<p>Специальный ремонт фасада вместил следующие работы:</p>
<ol>
<li>демонтаж штукатурного покрытия, разборка старых и установка новых входных групп;</li>
<li>нанесение армирующего слоя штукатурки с теплосеткой;</li>
<li>финишная отделка фасада с использованием эластичной напыляемой штукатурки.</li>
</ol>
<p>Все этапы работ и конечный результат представлены на фото. Специальный ремонт фасада занял 3 недели.</p>

