				<h2>Мокрый фасад под ключ: устройство и используемые материалы</h2>
				<p>
					 Облицовка включает в себя следующие слои:
				</p>
				<ul>
					<li>теплоизоляционную прокладку. В качестве материала применяют либо минеральную вату достаточно высокой плотности, либо пенопласт толщиной 50–100 мм. Отличие одного утеплителя от другого — в цене за метр и некоторых эксплуатационных характеристиках. Пенопластовые плиты дешевле, отличаются низкой теплопроводностью и простотой монтажа. Минераловатные плиты труднее обрабатывать, но коэффициент паропроницаемости у них значительно выше, что позволяет стенам «дышать» в процессе эксплуатации; </li>
					<li>армирующий слой. Как правило, используют специальную строительную сетку, которая поставляется в рулонах шириной 1 метр. Благодаря такой сетке на утеплитель можно фиксировать штукатурный раствор. </li>
					<li>клеевой состав для крепления теплоизолятора и армирующей сетки. При устройстве мокрого фасада утеплитель и сетку сначала монтируют на стену при помощи клея. Для этих целей используют два вида клеящих составов: универсальные и специализированные. Первые подходят для монтажа теплоизоляционных материалов и армирующей сетки. Вторые используются только для армирования утеплителя. </li>
					<li>механическое крепление для теплоизоляционных плит. В этом слое используются крепежи типа «зонт» — пластиковые дюбели, которые имеют широкую шляпку. На одну секцию утеплителя уходит примерно по 5 штук креплений. Назначение «зонта» — в дополнительной фиксации материала при обустройстве мокрого фасада. </li>
				</ul>
