				<p>
					 Обустройство стен по данной технологии включает следующие этапы:
				</p>
				<ol>
					<li>Измерение кривизны стен. На предварительной стадии строительства мокрого фасада следует определить степень искривления поверхности. Для этого вывешиваются отвесы и натягиваются горизонтальные нити, которые служат маяками. При движении этой системы вдоль стены замеряется пространство для утеплителя и клея. </li>
					<li>Фиксацию утеплителя на клей. Вяжущий состав наносится на пенопласт или минеральную плиту небольшими горками в середине и по краям. После этого материал приклеивается к стене. При обустройстве мокрого фасада пенопласт желательно фиксировать на стену вразбежку (стыки должны иметь небольшую длину). Швы необходимо заполнять клеем сразу в процессе укладки утеплителя. </li>
					<li>Установку механических креплений. Следующий этап строительства мокрого фасада заключается в монтаже пластиковых зонтиков. С помощью перфоратора проделываются отверстия в стене и молотком забиваются дюбели. Важно правильно расположить крепеж. По технологии делают так: один дюбель — в середине плиты, а другие четыре — по углам. Чтобы сэкономить на стоимости фасада, можно вбивать «зонт» в шов между плитами утеплителя. </li>
					<li>Армирование. Сетка приклеивается к пенопласту или минеральным плитам методом вдавливания в клей. Вяжущий состав наносится на утеплитель, после чего сверху кладется сетка и придавливается. Остатки клея удаляются шпателем. </li>
					<li>Нанесение штукатурки. Завершающий этап обустройства мокрого фасада — оштукатуривание поверхности. Зачастую черновую отделку следует нанести в несколько слоев, чтобы тщательно выровнять стену. После высыхания шпатлевки можно приступать к нанесению финишной отделки. </li>
				</ol>
